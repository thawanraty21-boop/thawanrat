#โปรแกรมคำนวณค่าคอมมิชชั่นของพนักงานขาย โดยป้อนรหัสพนักงาน ชื่อพนักงาน และจำนวนเงินซึ่งเป็นยอดขายของพนักงาน จากผู้ใช้ทางแป้นพิมพ์ และคำนวณค่าคอมมิชชั่น จากเงื่อนไข ดังนี้
#ขายของได้ไม่เกิน 1000 บาท ค่าคอมมิชชั่น เป็น 0.0 บาท
#ขายของได้ตั้งแต่ 1001 – 2000  บาท ค่าคอมมิชชั่นคิด   1% จากยอดขาย
#ขายของได้ตั้งแต่ 2001 – 3000  บาท ค่าคอมมิชชั่นคิด   3% จากยอดขาย
#ขายของได้ตั้งแต่ 3001  บาท ขึ้นไป ค่าคอมมิชชั่นคิด   5% จากยอดขาย
print('---------------------')
print('โปรแกรมคำนวณค่าคอมมิชั่น')
print('---------------------')

employee_id = input("ป้อนรหัสพนักงาน: ")
employee_name = input("ป้อนชื่อพนักงาน: ")
  
while True:
    try:
      sales_amount = float(input("ป้อนยอดขาย (บาท): "))
      if sales_amount >= 0:
        break
      else:
        print("ยอดขายต้องไม่เป็นค่าติดลบ กรุณาป้อนใหม่")
    except ValueError:
      print("ข้อมูลไม่ถูกต้อง! กรุณาป้อนยอดขายเป็นตัวเลข")

commission = 0.0
if sales_amount <= 1000:
    commission = 0.0
elif sales_amount <= 2000:
    commission = sales_amount * 0.01  # 1%
elif sales_amount <= 3000:
    commission = sales_amount * 0.03  # 3%
else:  # sales_amount > 3000
    commission = sales_amount * 0.05  # 5%

print("=========================================")
print("    สรุปข้อมูลพนักงานและค่าคอมมิชชั่น")
print("=========================================")
print(f"รหัสพนักงาน: {employee_id}")
print(f"ชื่อพนักงาน: {employee_name}")
print(f"ยอดขาย: {sales_amount:,.2f} บาท")
print(f"ค่าคอมมิชชั่นที่ได้รับ: {commission:,.2f} บาท")
print("=========================================")